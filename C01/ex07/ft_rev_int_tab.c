void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp;
	int	i1;
	int	i2;

	i1 = 0;
	i2 = size - 1;
	while (i1 < i2)
	{
		tmp = tab[i1];
		tab[i1] = tab[i2];
		tab[i2] = tmp;
		i1++;
		i2--;
	}
}
