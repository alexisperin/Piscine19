#include "ft_cat.h"

int	main(int argc, char **argv)
{
	int	i;

	if (argc == 1)
		ft_stdin();
	i = 1;
	while (i < argc)
	{
		ft_cat(argv[0], argv[i]);
		i++;
	}
}
